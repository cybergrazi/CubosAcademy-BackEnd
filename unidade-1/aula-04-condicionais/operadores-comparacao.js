// Introdução aos comparadores de comparação (maior e menor que...).

const ehMaior = 25 > 16; //Maior que
console.log(ehMaior);

const ehMenor = 15 < 16; //Menor que
console.log(ehMenor);

const maiorIgual = 16 >= 16; //Maior ou igual que
console.log(maiorIgual);

const menorIgual = 15 <= 16; //Menor ou igual que
console.log(menorIgual);

