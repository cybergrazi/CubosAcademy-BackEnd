// Introdução aos operadores aritméticos do JavaScript

// Declarando 2 variáveis na mesma linha:
let x = 10, y = 2;

let resultado = x + y;
console.log(`A soma é ${resultado}`);

resultado = x - y;
console.log(`A subtração é ${resultado}`);

resultado = x * y;
console.log(`A multiplicação é ${resultado}`);

resultado = x / y;
console.log(`A divisão é ${resultado}`);