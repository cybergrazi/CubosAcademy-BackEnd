// Introdução às funções com parâmetros.
// Criar uma função que cumprimente alguém.

function cumprimentar(pessoa, idade) {
    console.log(`Olá ${pessoa}! Fiquei sabendo que você tem ${idade} anos.`);
}

const nome = "Grazi";
cumprimentar(nome, 18); // dentro dos parênteses é o argumento da função
cumprimentar("Carol", 20);
cumprimentar("Gabriel", 28);
cumprimentar("Luna", 7);
cumprimentar("Ludmilla", 60);